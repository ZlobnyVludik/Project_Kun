# Средствами языка Python сформировать текстовый файл (.txt), содержащий 
# последовательность из целых положительных и отрицательных чисел. Сформировать 
# новый текстовый файл (.txt) следующего вида, предварительно выполнив требуемую 
# обработку элементов:
# Исходные данные:
# Количество элементов:
# Индекс последнего минимального элемента:
# Сумма элементов больших 10 во второй половине: 

# ---------------------------------------------
# Программа работы с текстовыми файлами и числами
# ---------------------------------------------

import random

# ---------- 1. Создаем исходный файл с числами ----------

# Сгенерируем случайную последовательность целых чисел
numbers = [random.randint(-50, 50) for _ in range(20)]

# Записываем числа в файл (через пробел)
with open("input.txt", "w", encoding="utf-8") as f:
    for num in numbers:
        f.write(str(num) + " ")

# ---------- 2. Читаем числа из файла ----------

with open("input.txt", "r", encoding="utf-8") as f:
    # split() разбивает строку по пробелам
    data = f.read().split()

# преобразуем строки в целые числа
numbers = [int(x) for x in data]

# ---------- 3. Обработка данных ----------

# Количество элементов
count = len(numbers)

# Минимальное значение
min_value = min(numbers)

# Индекс последнего минимального элемента
# ищем с конца списка
last_min_index = len(numbers) - 1 - numbers[::-1].index(min_value)

# Вторая половина последовательности
second_half = numbers[count // 2:]

# Сумма элементов > 10 во второй половине
sum_gt_10 = sum(x for x in second_half if x > 10)

# ---------- 4. Запись результата в новый файл ----------

with open("output.txt", "w", encoding="utf-8") as f:
    f.write("Исходные данные:\n")
    f.write(" ".join(map(str, numbers)) + "\n\n")

    f.write(f"Количество элементов: {count}\n")
    f.write(f"Индекс последнего минимального элемента: {last_min_index}\n")
    f.write(f"Сумма элементов больших 10 во второй половине: {sum_gt_10}\n")

print("\nИсходные данные:")
print(*numbers)

print("Количество элементов:", count)
print("Индекс последнего минимального элемента:", last_min_index)
print("Сумма элементов больших 10 во второй половине:", sum_gt_10)
